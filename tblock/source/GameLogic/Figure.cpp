int Figure::GetBlockType( const int figure, const int rotation, const int x, const int y ) const
{
	return piecesArray[figure][rotation][x][y];
}

int Figure::GetXInitialPosition( const int figure, const int rotation ) const
{
	return piecesArrayInitialPosition [figure][rotation][0];
}

int Figure::GetYInitialPosition( const int figure, const int rotation ) const
{
	return piecesArrayInitialPosition[figure][rotation][1];
}